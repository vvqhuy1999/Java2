   private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
      // TODO add your handling code here:
      char[] correctPass = "polytechnic".toCharArray();
      if(checkNull()){
         String user = txtuser.getText();
         char[] pass = txtpass.getPassword();
         boolean check = chkbox1.isSelected();
         
         if(user.equals("fpt") && Arrays.equals(pass,correctPass)){
            JOptionPane.showMessageDialog(this, "Ban Login thanh cong");
           
            if(!check){
               txtuser.setText("");
               txtpass.setText("");
               JOptionPane.showMessageDialog(this, "Tai Khoan ban Khong ghi nho");
            }else{
               JOptionPane.showMessageDialog(this, "Tai Khoan ban da ghi nho");
            }
         }
         else{
            JOptionPane.showMessageDialog(this, "Ban Login that bai");
            if(!check){
               txtuser.setText("");
               txtpass.setText("");
               JOptionPane.showMessageDialog(this, "Tai Khoan ban Khong ghi nho");
            }
         }
      }
 
   }//GEN-LAST:event_btnloginActionPerformed

